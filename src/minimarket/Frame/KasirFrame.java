    public void loadTable(ResultSet rs){
        tb_product1.setModel(DbUtils.resultSetToTableModel(rs));
        
        TableColumnModel tcm = tb_product1.getColumnModel();
        tcm.getColumn(0).setHeaderValue("Product ID");
        tcm.getColumn(1).setHeaderValue("Name");
        tcm.getColumn(2).setHeaderValue("Type");
        tcm.getColumn(3).setHeaderValue("Stock");
        tcm.getColumn(4).setHeaderValue("Price");
        tcm.getColumn(5).setHeaderValue("Expired_Date");         
    }
    
