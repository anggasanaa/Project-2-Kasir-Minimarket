    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        // TODO add your handling code here:                                      //SUBMIT BUTTON
        try {
        
        String user_id = tf_user.getText();
        String product_name = tf_product.getText();
        String totalq = tf_quantity.getText();
        String harga = tf_harga.getText();
        String totalp = tf_totalharga.getText();
        
        model.setUser_id(user_id);
        model.setProduct_id(product_name);
        model.setTotalq(totalq);
        model.setHarga(harga);        
        model.setTotalp(totalp);
        
        boolean result = controller.submit(model);
        if(result){
            JOptionPane.showMessageDialog(null, "Produk " + product_name
             + " Dengan Harga " + totalp + ". Terima Kasih Sudah Berbelanja Di Minimarket Kami");
        }
        else{
            JOptionPane.showMessageDialog(null, "Gagal Melakukan transaksi");
        }
        Clear();
        getAllData1();
        } catch (Exception ex) {
            System.err.println(ex.getMessage());
        }
    }//GEN-LAST:event_btn_submitActionPerformed

