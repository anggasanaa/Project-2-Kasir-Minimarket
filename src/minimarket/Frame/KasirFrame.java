    private void tb_belanjaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_belanjaMouseClicked
        // TODO add your handling code here:
       try{
            String id = helper.getValueRows(tb_belanja, 0);
            String user = helper.getValueRows(tb_belanja, 1);
            String product = helper.getValueRows(tb_belanja, 2);
            String jumlah = helper.getValueRows(tb_belanja, 3);
            String harga = helper.getValueRows(tb_belanja, 4);
            String total = helper.getValueRows(tb_belanja, 5);
            
            btn_delete.setEnabled(true);
            
            this.product_id = id;
            tf_user.setText(user);
            tf_product.setText(product);            
            tf_quantity.setText(jumlah);
            tf_harga.setText(harga);
            tf_totalharga.setText(total);   
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
        auto_sum();

    }//GEN-LAST:event_tb_belanjaMouseClicked

